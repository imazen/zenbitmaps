// ══════════════════════════════════════════════════════════════════════
// HDR — lossy RGBE, verify within tolerance
// ══════════════════════════════════════════════════════════════════════

#[cfg(feature = "hdr")]
#[test]
fn fixture_hdr() {
    let data = fixture("float.hdr");
    assert!(data.starts_with(b"#?R"));
    let decoded = decode_hdr(&data, Unstoppable).unwrap();
    assert_eq!((decoded.width, decoded.height), (8, 8));
    assert_eq!(decoded.layout, PixelLayout::RgbF32);
    // Verify top-left pixel is red-ish (R high, G/B low)
    let px = decoded.pixels();
    let r = f32::from_le_bytes(px[0..4].try_into().unwrap());
    let g = f32::from_le_bytes(px[4..8].try_into().unwrap());
    assert!(r > g, "top-left should be red: r={r}, g={g}");
}

