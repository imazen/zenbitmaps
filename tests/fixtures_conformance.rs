//! Cross-format conformance tests using ImageMagick-generated fixtures.
//!
//! All fixtures encode the same 8x8 image with red/green/blue/white quadrants.
//! Tests verify each format decodes without error, produces correct dimensions,
//! and (where applicable) exact pixel match against P6 binary PPM reference.
//!
//! ImageMagick applies colorspace transforms for grayscale and HDR output,
//! so those are tested for reasonable values rather than exact match.

use enough::Unstoppable;
use zenbitmaps::*;

fn fixture(name: &str) -> Vec<u8> {
    let path = format!("tests/fixtures/{name}");
    std::fs::read(&path).unwrap_or_else(|e| panic!("failed to read {path}: {e}"))
}

fn reference_rgb8() -> Vec<u8> {
    let data = fixture("p6_binary.ppm");
    let decoded = decode(&data, Unstoppable).unwrap();
    assert_eq!((decoded.width, decoded.height), (8, 8));
    assert_eq!(decoded.layout, PixelLayout::Rgb8);
    decoded.pixels().to_vec()
}

// ══════════════════════════════════════════════════════════════════════
// PNM family — all decode correctly, P3/P6 match exactly
// ══════════════════════════════════════════════════════════════════════

#[test]
fn fixture_p1_ascii_pbm() {
    let data = fixture("p1_ascii.pbm");
    let decoded = decode(&data, Unstoppable).unwrap();
    assert_eq!((decoded.width, decoded.height), (8, 8));
    assert_eq!(decoded.layout, PixelLayout::Gray8);
    assert!(decoded.pixels().iter().all(|&p| p == 0 || p == 255));
}

#[test]
fn fixture_p2_ascii_pgm() {
    let data = fixture("p2_ascii.pgm");
    let decoded = decode(&data, Unstoppable).unwrap();
    assert_eq!((decoded.width, decoded.height), (8, 8));
    assert_eq!(decoded.layout, PixelLayout::Gray8);
    // Just verify reasonable grayscale values
    assert!(decoded.pixels().iter().any(|&p| p > 200)); // white quadrant
    assert!(decoded.pixels().iter().any(|&p| p < 50));  // dark quadrants
}

#[test]
fn fixture_p3_ascii_ppm() {
    let data = fixture("p3_ascii.ppm");
    let decoded = decode(&data, Unstoppable).unwrap();
    assert_eq!((decoded.width, decoded.height), (8, 8));
    assert_eq!(decoded.layout, PixelLayout::Rgb8);
    // P3 and P6 should produce identical pixels (same image, different encoding)
    assert_eq!(decoded.pixels(), &reference_rgb8()[..]);
}

#[test]
